package collection;

public class PersonalDetail {

    private String personalEmail;
    private String nativeOrigin;
    private String bloodGroup;
    private String nameOfSpouse;
    private int numberOfChlidren;

    public String getPersonalEmail() {
        return personalEmail;
    }

    public void setPersonalEmail(String personalEmail) {
        this.personalEmail = personalEmail;
    }

    public String getNativeOrigin() {
        return nativeOrigin;
    }

    public void setNativeOrigin(String nativeOrigin) {
        this.nativeOrigin = nativeOrigin;
    }

    public String getBloodGroup() {
        return bloodGroup;
    }

    public void setBloodGroup(String bloodGroup) {
        this.bloodGroup = bloodGroup;
    }

    public String getNameOfSpouse() {
        return nameOfSpouse;
    }

    public void setNameOfSpouse(String nameOfSpouse) {
        this.nameOfSpouse = nameOfSpouse;
    }

    public int getNumberOfChlidren() {
        return numberOfChlidren;
    }

    public void setNumberOfChlidren(int numberOfChlidren) {
        this.numberOfChlidren = numberOfChlidren;
    }
}
